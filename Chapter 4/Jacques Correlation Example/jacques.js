/*
  Process each journal entry and loop over every event in that entry
  If event is not already stored in phis, compute correlation and add to phis
*/
function gatherCorrelations(journal) {
  var phis = {};
  for (var entry = 0; entry < journal.length; entry++) {
    var events = journal[entry].events;
    for (var i=0; i < events.length; i++) {
      var event = events[i];
      if (!(event in phis))
        phis[event] = phi(tableFor(event, journal));
    }
  }
  return phis;
}

function showAllCorrelations(correlations) {
  for (var event in correlations) {
    console.log(event + " : " + correlations[event]);
  }
}

function showSignificantCorrelations(correlations) {
  for(var event in correlations) {
    var correlation = correlations[event];
    if(correlation > 0.1 || correlation < -0.1)
      console.log(event + " : " + correlation);
  }
}

