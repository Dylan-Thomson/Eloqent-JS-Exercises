/*
  Add new entry to journal
*/
function addEntry(events, didITurnIntoASquirrel) {
  journal.push({
    events: events,
    squirrel: didITurnIntoASquirrel
  });
}

/*
  Compute correlation (phi coefficient)
  Takes 1D array representing 4x4 table
  -1 means negative correlation
  1 means positive correlation
  0 means no correlation whatsoever
*/
function phi(table) {
  return (table[3] * table[0] - table[2] * table[1]) /
    Math.sqrt((table[2] + table[3]) *
              (table[0] + table[1]) *
              (table[1] + table[3]) *
              (table[0] + table[2]));
}

/*
  Test whether provided journal entry contains provided event
*/
function hasEvent(event, entry) {
  return entry.events.indexOf(event) != -1;
}


/*
  Return 1D array representing 4x4 table for an event to be used in function phi

  Here is how the array works:
    Binary 00 or element 0 represents instances of no event + no squirrel
    Binary 01 or element 1 represents instances of event + no squirrel
    Binary 10 or element 2 represents instances of no event + squirrel
    Binary 11 or element 3 represents instances of event + squirrel

  So considering the output for tableFor("pizza", journal) [76, 9, 4, 1]
    76 entries where he did not eat pizza and did not transform
    9 entries where he ate pizza and did not transform
    4 entries where he did not eat pizza and did transform
    1 entry where he did eat pizza and did transform

*/
function tableFor(event, journal) {
  var table = [0, 0, 0, 0];
  for (var i = 0; i < journal.length; i++) {
    var entry = journal[i], index = 0;
    if (hasEvent(event, entry)) index += 1;
    if (entry.squirrel) index += 2;
    table[index] += 1;
  }
  return table;
}

